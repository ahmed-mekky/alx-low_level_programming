#include "calc.h"

/**
 * op_add - add two integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: add of the two integers
 */
int op_add(int a, int b)
{
	return (a + b);
}

/**
 * op_sub - add two integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: sub of the two integers
 */
int op_sub(int a, int b)
{
	return (a - b);
}

/**
 * op_mul - add two integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: mul of the two integers
 */
int op_mul(int a, int b)
{
	return (a * b);
}

/**
 * op_div - add two integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: div of the two integers
 */
int op_div(int a, int b)
{
	return (a / b);
}

/**
 * op_mod - add two integers
 *
 * @a: first int
 * @b: second int
 *
 * Return: mod of the two integers
 */
int op_mod(int a, int b)
{
	return (a % b);
}
